import SwiftUI

extension Color {
   enum VkTheme {
      static var brandBlue: Color {
         return Color("brandBlue")
      }
      static var brandGray: Color {
         return Color("brandGray")
      }
      static var brandLightBlue: Color {
         return Color("brandLightBlue")
      }
      static var secondaryBackground: Color {
         return Color("secondaryBackground")
      } 
      
      static var primaryTextColor: Color {
         return Color("primaryTextColor")
      }
   }
}
